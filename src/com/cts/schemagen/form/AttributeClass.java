package com.cts.schemagen.form;

public class AttributeClass {
private String attributeName ;
private String attributeType;
private String attributeParent;
public String getAttributeName() {
	return attributeName;
}
public void setAttributeName(String attributeName) {
	this.attributeName = attributeName;
}
public String getAttributeType() {
	return attributeType;
}
public void setAttributeType(String attributeType) {
	this.attributeType = attributeType;
}
public String getAttributeParent() {
	return attributeParent;
}
public void setAttributeParent(String attributeParent) {
	this.attributeParent = attributeParent;
}



}
